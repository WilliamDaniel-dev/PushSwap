#include <unistd.h>

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (s == NULL || !fd)
	{
		write(1, "(null)", 6);
	}
	while (*s)
	{
		write(fd, s++, sizeof(char));
		i++;
	}
}
